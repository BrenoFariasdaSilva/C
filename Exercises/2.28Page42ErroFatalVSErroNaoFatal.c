/*

2.28 Faça a distinção entre os termos erro fatal e erro não fatal.
Por que seria preferível experimentar um erro fatal
em vez de um erro não fatal?

RESPOSTA:

É preferível cometer um erro fatal, pois o código será interrompido instantaneamente,
logo não será gerado um resultado errado. Além disso, erros fatais são identificados pelo Compilador
mais facilmente.

*/